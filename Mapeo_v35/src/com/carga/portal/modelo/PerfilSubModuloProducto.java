package com.carga.portal.modelo;

public class PerfilSubModuloProducto {
	
	private String codigoPerfil;
	private String codigoSubModulo;
	private String producto;
	
	public String getCodigoPerfil() {
		return codigoPerfil;
	}
	public void setCodigoPerfil(String codigoPerfil) {
		this.codigoPerfil = codigoPerfil;
	}
	public String getCodigoSubModulo() {
		return codigoSubModulo;
	}
	public void setCodigoSubModulo(String codigoSubModulo) {
		this.codigoSubModulo = codigoSubModulo;
	}
	public String getProducto() {
		return producto;
	}
	public void setProducto(String producto) {
		this.producto = producto;
	}
	
}