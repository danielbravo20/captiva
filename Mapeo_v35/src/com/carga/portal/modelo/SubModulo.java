package com.carga.portal.modelo;

public class SubModulo {
	
	private String codigoModulo;
	private String codigoSubModulo;
	private String descipcion;
	
	public String getCodigoModulo() {
		return codigoModulo;
	}
	public void setCodigoModulo(String codigoModulo) {
		this.codigoModulo = codigoModulo;
	}
	public String getCodigoSubModulo() {
		return codigoSubModulo;
	}
	public void setCodigoSubModulo(String codigoSubModulo) {
		this.codigoSubModulo = codigoSubModulo;
	}
	public String getDescipcion() {
		return descipcion;
	}
	public void setDescipcion(String descipcion) {
		this.descipcion = descipcion;
	}
	
}