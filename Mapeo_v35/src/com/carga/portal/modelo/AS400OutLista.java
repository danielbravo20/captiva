package com.carga.portal.modelo;

import java.util.List;

public class AS400OutLista {
	
	private String nombre;
	private int times;
	private int inicio;
	private int longitud;
	private String objetoDestino;
	private String objetoDestinoLista;
	
	private List<AS400Out> as400outs;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getTimes() {
		return times;
	}
	public void setTimes(int times) {
		this.times = times;
	}
	public int getInicio() {
		return inicio;
	}
	public void setInicio(int inicio) {
		this.inicio = inicio;
	}
	public List<AS400Out> getAs400outs() {
		return as400outs;
	}
	public void setAs400outs(List<AS400Out> as400outs) {
		this.as400outs = as400outs;
	}
	public int getLongitud() {
		return longitud;
	}
	public void setLongitud(int longitud) {
		this.longitud = longitud;
	}
	public String getObjetoDestino() {
		return objetoDestino;
	}
	public void setObjetoDestino(String objetoDestino) {
		this.objetoDestino = objetoDestino;
	}
	public String getObjetoDestinoLista() {
		return objetoDestinoLista;
	}
	public void setObjetoDestinoLista(String objetoDestinoLista) {
		this.objetoDestinoLista = objetoDestinoLista;
	}
	
}
